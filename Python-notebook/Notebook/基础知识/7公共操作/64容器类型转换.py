'''
容器类型转换：
    1.tuple() -- 将某个序列转换为元组
    2.list() -- 将某个序列转换为列表
    3.set() -- 将某个序列转换为集合

容器类型转换的目的：
    想要的容器类型和程序中已有的数据类型不一致
'''

list1 = [10, 20, 30, 40, 50, 50]
s1 = {100, 300, 200, 500}
t1 = ('a', 'b', 'c', 'd', 'e')

# 1.tuple() -- 转换为元组
print(tuple(list1)) # 返回 (10, 20, 30, 40, 50, 50)
print(tuple(s1)) # 返回 (200, 100, 500, 300)

# 2.list() -- 转换为列表
print(list(s1)) # 返回 [200, 100, 500, 300]

# 3.set() -- 转换为集合
print(set(list1)) # 返回 {40, 10, 50, 20, 30} -- 数据中只有1个50，源于集合的去重功能

