'''
1.1 基本数据类型-数值型
1.1.1 基本数据类型-数值型-整数类型

进制种类    引导符号        描述
十进制      无             默认情况, 例：1100, -1100
二进制      0b 或 0B       由字符 0 和 1 组成, 例：0b1010, 0B1010
八进制      0o 或 0O       由字符 0 到 7 组成, 例：0o010, 0O1010
十六进制    0x 或 0X       由字符 0~9、a~f、A~F 组成, 例：0x1010, 0X1010  

附：进制转换
    - 二进制转十进制 -- 1010 = 1 * 2^3 + 0 * 2^2 + 1 * 2^1 + 0 * 2^0 = 8 + 0 + 2 +0 = 10
    - 八进制转十进制 -- 167 = 1 * 8^2 + 6 * 8^1 + 7 * 8^0 = 64 + 48 + 7 = 119
    - 十六进制转十进制 -- 24B = 2 * 16^2 + 4 * 16^1 + 11 * 16^0 = 512 + 64 + 11 = 587
'''
print(bin(10)) # return 0b1010
print(oct(119)) # return 0o167
print(hex(587)) # return 0x24B


