'''
1.1.3 基本数据类型-数值型-复数类型

复数 a+bj, 其中a是实数部分, 简称实部, b是虚数部分, 简称虚部

虚数部分通过后缀 j 或 J 来表示

j ** 2 = -1, j 为虚数单位; 当 b = 1 时, 1 不能省略, 即 1j

11.3 + 4j

附：对于复数z, z.real 获取实数部分, z.imag 获取虚数部分
'''
a = 11.3 + 4j
print(a.real) # return 11.3
print(a.imag) # return 4.0


